/**
 * Expects a two-digit country code. Returns an object with the name and native name for that country. If not found, it returns undefined.
 */
export const getLangObject = (abreviation) => {
  const key =
    abreviation === 'zh-CN'
      ? 'zhCN'
      : abreviation === 'zh-TW'
      ? 'zhTW'
      : abreviation
  const languageObject = languages[key]
  return languageObject ? languageObject : undefined
}

/**
 * Expects a two-digit country code. Returns the name for that country. If not found, it returns undefined.
 */
export const getName = (abreviation) => {
  const languageObject = getLangObject(abreviation)
  return languageObject ? languageObject.name : undefined
}

/**
 * Expects a two-digit country code. Returns the native name for that country. If not found, it returns undefined.
 */
export const getNativeName = (abreviation) => {
  const languageObject = getLangObject(abreviation)
  return languageObject ? languageObject.native : undefined
}
